package com.example.studenthelpermobile.Model;

public class ChatTitle {

    private String title;
    private int count;

    public ChatTitle() {
        title = "";
        count = 0;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}

